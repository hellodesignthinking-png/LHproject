    @classmethod
    def get_price(cls, gu: str, dong: str = None) -> int:
        """
        구·동별 시세 조회
        
        Args:
            gu: 구 이름 (예: "마포구")
            dong: 동 이름 (선택)
            
        Returns:
            ㎡당 단가 (원)
        """
        
        if gu not in cls.MARKET_PRICES:
            return 9000000  # 기본값
        
        gu_data = cls.MARKET_PRICES[gu]
        
        # 동 지정 시
        if dong and dong != '알수없음' and dong in gu_data.get('dong', {}):
            return gu_data['dong'][dong]
        
        # 구 평균
        return gu_data['average']
    
    @classmethod
    def get_pyeong_price(cls, gu: str, dong: str = None) -> int:
        """
        평당 단가 조회
        
        Returns:
            평당 단가 (원)
        """
        sqm_price = cls.get_price(gu, dong)
        return int(sqm_price * 3.3058)
