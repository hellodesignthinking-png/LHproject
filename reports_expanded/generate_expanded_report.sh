### 6.3.2 에너지 절감률 계산

```
에너지 절감률 (%) = (기준 에너지 - 실제 에너지) / 기준 에너지 × 100

기준 에너지 = 「건축물의 에너지절약설계기준」에 따른 의무 기준
실제 에너지 = 에너지 성능 시뮬레이션 결과 (ECO2, EnergyPlus 등)
```

**ZeroSite 구현 (추정)**:
```python
def calculate_energy_savings(zone_type, actual_energy):
    # 기준 에너지 (의무 기준)
    baseline_energy = {
        '제1종일반주거지역': 180,
        '제2종일반주거지역': 180,
        '제3종일반주거지역': 180,
        '준주거지역': 180
    }
    
    baseline = baseline_energy.get(zone_type, 180)
    savings_rate = (baseline - actual_energy) / baseline * 100
    
    return max(0, savings_rate)
```

