## 6.2 탄소배출량 산정

### 6.2.1 건축물 탄소배출량 산식

**건축물 생애주기 탄소배출량 (LCA: Life Cycle Assessment)**

```
총 탄소배출량 (kgCO₂eq) = 자재생산 + 운송 + 시공 + 운영 + 해체

자재생산 배출량 = Σ(자재 중량i × 탄소배출계수i)
운영 배출량 (연간) = 에너지사용량 (kWh/㎡·년) × 전력 탄소배출계수
```

**ZeroSite 적용 산식 (간소화)**:
```python
def calculate_carbon_emission(building_area, energy_efficiency_grade):
    # 기준 연간 에너지 사용량 (kWh/㎡·년)
    base_energy = {
        '1등급': 60,
        '2등급': 90,
        '3등급': 120,
        '4등급': 150,
        '5등급': 180
    }
    
    # 전력 탄소배출계수 (2024년 기준: 0.4533 kgCO₂eq/kWh)
    carbon_factor = 0.4533
    
    # 연간 탄소배출량
    annual_emission = building_area * base_energy[energy_efficiency_grade] * carbon_factor
    
    # 30년 생애주기 탄소배출량
    lifecycle_emission = annual_emission * 30
    
    return {
        'annual_emission_kg': annual_emission,
        'lifecycle_emission_tons': lifecycle_emission / 1000
    }
```

**예시 계산 (서울 800㎡, 에너지효율 1등급)**:
```
건축 연면적: 1,600㎡ (용적률 200%)
연간 에너지 사용량: 60 kWh/㎡·년
연간 탄소배출량: 1,600 × 60 × 0.4533 = 43,516 kgCO₂eq (43.5톤)
30년 생애주기: 43.5 × 30 = 1,305톤 CO₂eq
```

