### 6.4.2 태양광 발전 설치 면적 산정

**필요 설치 면적 계산**:
```
태양광 설치 면적 (㎡) = 목표 발전량 (kWh/년) / (태양광 패널 효율 × 일사량 × 시스템 효율)

태양광 패널 효율: 0.20 (20%, 일반적인 다결정 실리콘)
일사량: 서울 기준 4.0 kWh/㎡·day = 1,460 kWh/㎡·년
시스템 효율: 0.80 (인버터 손실, 온도 손실 등)
```

**예시 계산 (서울 1,600㎡ 건물, 에너지효율 1등급)**:
```
연간 에너지 사용량: 1,600 × 60 = 96,000 kWh/년
신재생 목표 (15%): 96,000 × 0.15 = 14,400 kWh/년

태양광 설치 면적 = 14,400 / (0.20 × 1,460 × 0.80)
                 = 14,400 / 233.6
                 = 61.6 ㎡

옥상 면적 (800㎡) 대비: 61.6 / 800 = 7.7%
```

**ZeroSite 구현**:
```python
def calculate_solar_panel_area(total_energy, renewable_ratio=0.15):
    target_generation = total_energy * renewable_ratio
    
    panel_efficiency = 0.20
    solar_irradiation = 1460  # kWh/㎡·년 (서울 기준)
    system_efficiency = 0.80
    
    required_area = target_generation / (panel_efficiency * solar_irradiation * system_efficiency)
    
    return {
        'target_generation_kwh': target_generation,
        'required_area_sqm': round(required_area, 1),
        'panel_count': int(required_area / 1.7)  # 1.7㎡/패널 가정
    }
```

